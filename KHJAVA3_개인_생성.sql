-- 게시판

CREATE TABLE EMP_BOARD(
    REV_NUM           NUMBER          NOT NULL, 
    REV_TITLE         VARCHAR2(20)    NOT NULL, 
    REV_CONTENT       VARCHAR2(20)    NOT NULL, 
    REV_IMG_NAME      VARCHAR2(20)    NULL, 
    MEM_EMAIL         VARCHAR2(20)    NOT NULL, 
    REV_LOOK_CNT      NUMBER          NULL, 
    REV_WRITE_DATE    DATE            NOT NULL, 
    CONSTRAINT EMP_BOARD_PK PRIMARY KEY (REV_NUM),
    CONSTRAINT FK_EMP_BOARD_MEM_EMAIL_REC FOREIGN KEY (MEM_EMAIL) REFERENCES EMP_INFO (MEM_EMAIL)
);

COMMENT ON TABLE EMP_BOARD IS '개인회원 전용-기업 평가/경험담 작성 게시판(자유게시판)';

COMMENT ON COLUMN EMP_BOARD.REV_NUM IS '평가 글 번호';

COMMENT ON COLUMN EMP_BOARD.REV_TITLE IS '평가 제목';

COMMENT ON COLUMN EMP_BOARD.REV_CONTENT IS '평가 내용';

COMMENT ON COLUMN EMP_BOARD.REV_IMG_NAME IS '첨부파일';

COMMENT ON COLUMN EMP_BOARD.MEM_EMAIL IS '작성자';

COMMENT ON COLUMN EMP_BOARD.REV_LOOK_CNT IS '조회수';

COMMENT ON COLUMN EMP_BOARD.REV_WRITE_DATE IS '작성일';

