-- 이력서 모음
CREATE TABLE EMP_RESUME(
    RESUME_NUM         NUMBER           NOT NULL, 
    RESUME_TITLE       VARCHAR2(50)     NOT NULL, 
    RESUME_CONTENT     VARCHAR2(500)    NOT NULL, 
    MEM_EMAIL          VARCHAR2(20)     NOT NULL, 
    RESUME_ACADEMY     VARCHAR2(20)     NULL, 
    RESUME_CAREER      VARCHAR2(100)    NULL, 
    RESUME_LOC         VARCHAR2(100)    NULL, 
    RESUME_JOB         VARCHAR2(40)     NULL, 
    RESUME_TIME        VARCHAR2(20)     NULL, 
    RESUME_DATE        DATE             NULL, 
    RESUME_ANISAL      NUMBER           NULL, 
    RESUME_IMG         VARCHAR2(20)     NULL, 
    CERTIFICATION      VARCHAR2(500)    NULL, 
    RESUME_WISH_JOB    VARCHAR2(20)     NULL, 
    CONSTRAINT EMP_RESUME_PK PRIMARY KEY (RESUME_NUM),
    CONSTRAINT FK_EMP_RESUME_MEM_EMAIL_RE FOREIGN KEY (MEM_EMAIL) REFERENCES EMP_INFO (MEM_EMAIL)
);

COMMENT ON TABLE EMP_RESUME IS '이력서 모음';

COMMENT ON COLUMN EMP_RESUME.RESUME_NUM IS '이력서번호';

COMMENT ON COLUMN EMP_RESUME.RESUME_TITLE IS '이력서 제목';

COMMENT ON COLUMN EMP_RESUME.RESUME_CONTENT IS '이력서 내용';

COMMENT ON COLUMN EMP_RESUME.MEM_EMAIL IS '이메일(아이디)';

COMMENT ON COLUMN EMP_RESUME.RESUME_ACADEMY IS '학력사항';

COMMENT ON COLUMN EMP_RESUME.RESUME_CAREER IS '경력사항';

COMMENT ON COLUMN EMP_RESUME.RESUME_LOC IS '희망근무처';

COMMENT ON COLUMN EMP_RESUME.RESUME_JOB IS '희망 업직종';

COMMENT ON COLUMN EMP_RESUME.RESUME_TIME IS '희망 근무시간';

COMMENT ON COLUMN EMP_RESUME.RESUME_DATE IS '희망 근무기간';

COMMENT ON COLUMN EMP_RESUME.RESUME_ANISAL IS '희망 연봉';

COMMENT ON COLUMN EMP_RESUME.RESUME_IMG IS '첨부파일';

COMMENT ON COLUMN EMP_RESUME.CERTIFICATION IS '자격증';

COMMENT ON COLUMN EMP_RESUME.RESUME_WISH_JOB IS '관심직종';

