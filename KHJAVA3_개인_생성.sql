-- 개인 회원가입

CREATE TABLE EMP_INFO(
    MEM_EMAIL        VARCHAR2(20)     NOT NULL, 
    MEM_NICKNAME     VARCHAR2(20)     NOT NULL, 
    MEM_PASS         VARCHAR2(20)     NOT NULL, 
    MEM_NAME         VARCHAR2(20)     NOT NULL, 
    MEM_AGE          NUMBER           NOT NULL, 
    MEM_GENDER       VARCHAR2(10)     NULL, 
    MEM_BIRTH        NUMBER           NULL, 
    MEM_TEL1         NUMBER           NOT NULL, 
    MEM_TEL2         NUMBER           NULL, 
    MEM_ADDR         VARCHAR2(100)    NOT NULL, 
    MEM_MERRY        VARCHAR2(5)      NULL, 
    MEM_YN           VARCHAR2(5)      NULL, 
    MEM_PASS_HINT    VARCHAR2(50)     NOT NULL, 
    MEM_JOIN_DATE    DATE             NOT NULL, 
    CONSTRAINT EMP_INFO_PK PRIMARY KEY (MEM_EMAIL)
);

COMMENT ON TABLE EMP_INFO IS '개인회원(회원가입)';

COMMENT ON COLUMN EMP_INFO.MEM_EMAIL IS '이메일(아이디)';

COMMENT ON COLUMN EMP_INFO.MEM_NICKNAME IS '닉네임';

COMMENT ON COLUMN EMP_INFO.MEM_PASS IS '회원 비밀번호';

COMMENT ON COLUMN EMP_INFO.MEM_NAME IS '이름';

COMMENT ON COLUMN EMP_INFO.MEM_AGE IS '나이';

COMMENT ON COLUMN EMP_INFO.MEM_GENDER IS '성별';

COMMENT ON COLUMN EMP_INFO.MEM_BIRTH IS '생년월일';

COMMENT ON COLUMN EMP_INFO.MEM_TEL1 IS '연락처1(휴대폰)';

COMMENT ON COLUMN EMP_INFO.MEM_TEL2 IS '연락처2(집전화)';

COMMENT ON COLUMN EMP_INFO.MEM_ADDR IS '주소';

COMMENT ON COLUMN EMP_INFO.MEM_MERRY IS '결혼여부';

COMMENT ON COLUMN EMP_INFO.MEM_YN IS '회원탈퇴여부';

COMMENT ON COLUMN EMP_INFO.MEM_PASS_HINT IS '비밀번호힌트';

COMMENT ON COLUMN EMP_INFO.MEM_JOIN_DATE IS '가입날짜';

